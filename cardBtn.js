
let likeBtn1 = document.querySelector('.fa-heart1');
likeBtn1.addEventListener('click', changeLike1);
function changeLike1() {
  if (likeBtn1.getAttribute('name') == 'heart-outline') {
    likeBtn1.setAttribute('name', 'heart');
  }
  else {
    likeBtn1.setAttribute('name', 'heart-outline');
  }
}

